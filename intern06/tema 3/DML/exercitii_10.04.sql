SELECT nume
  || ' '
  || prenume
AS NUME_COMPLET
FROM GL_ANGAJATI
WHERE lower(NUME
  ||PRENUME) LIKE '%a%a%a%' ;
  

--2
SELECT COUNT(*) AS NUMAR_OFICII,
  GL_ANGAJATI.NUME,
  GL_ANGAJATI.PRENUME
FROM GL_OFICII
JOIN GL_ANGAJATI
ON GL_OFICII.COD_MANAGER = GL_ANGAJATI.COD_ANGAJAT
GROUP BY GL_ANGAJATI.NUME,
  GL_ANGAJATI.PRENUME
HAVING COUNT(*) >=2;








